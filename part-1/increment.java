public class increment{
    public static void main(String[] args){

        System.out.println("Increment and decrement");
        int x=10;
        System.out.println(x);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        ++x;
        System.out.println(x);
        --x;
        System.out.println(x);
    }
}